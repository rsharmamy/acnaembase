package com.demos.telcodemo.bean;

public class BlockItem {
	
	private String gridPath;
	private String gridTitle;
	private String gridSubTitle;
	
	public String getGridPath() {
		return gridPath;
	}
	public void setGridPath(String gridPath) {
		this.gridPath = gridPath;
	}
	public String getGridTitle() {
		return gridTitle;
	}
	public void setGridTitle(String gridTitle) {
		this.gridTitle = gridTitle;
	}
	public String getGridSubTitle() {
		return gridSubTitle;
	}
	public void setGridSubTitle(String gridSubTitle) {
		this.gridSubTitle = gridSubTitle;
	}
}
